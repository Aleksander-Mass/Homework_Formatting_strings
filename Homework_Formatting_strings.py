# Для решения задачи с форматированием строк в разных стилях, напишем код для каждого случая:
#
# 1. Использование оператора % для форматирования строк:

# Пример входных данных
team1_num = 5
team2_num = 6
score_1 = 40
score_2 = 42
team1_time = 1552.5
team2_time = 2153.3
tasks_total = 82
time_avg = 45.2
challenge_result = 'Победа команды Волшебники данных!'

# Форматирование с использованием %
result1 = "В команде Мастера кода участников: %d !" % team1_num
result2 = "Итого сегодня в командах участников: %d и %d !" % (team1_num, team2_num)

print(result1)  # => "В команде Мастера кода участников: 5 !"
print(result2)  # => "Итого сегодня в командах участников: 5 и 6 !"

# 2. Использование метода .format():

# Форматирование с использованием .format()
result3 = "Команда Волшебники данных решила задач: {} !".format(score_2)
result4 = "Волшебники данных решили задачи за {:.1f} с !".format(team1_time)

print(result3)  # => "Команда Волшебники данных решила задач: 42 !"
print(result4)  # => "Волшебники данных решили задачи за 1552.5 с !"

# 3. Использование f-строк:


# Форматирование с использованием f-строк
result5 = f"Команды решили {score_1} и {score_2} задач."
result6 = f"Результат битвы: {challenge_result}"
result7 = f"Сегодня было решено {tasks_total} задач, в среднем по {time_avg:.1f} секунды на задачу!"

print(result5)  # => "Команды решили 40 и 42 задач."
print(result6)  # => "Результат битвы: победа команды Волшебники данных!"
print(result7)  # => "Сегодня было решено 82 задач, в среднем по 45.2 секунды на задачу!"

# Опционально: расчет результата соревнования
if score_1 > score_2 or (score_1 == score_2 and team1_time > team2_time):
    challenge_result = 'Победа команды Мастера кода!'
elif score_1 < score_2 or (score_1 == score_2 and team1_time < team2_time):
    challenge_result = 'Победа команды Волшебники данных!'
else:
    challenge_result = 'Ничья!'

print(f"Результат битвы: {challenge_result}") # => Результат битвы: Победа команды Волшебники данных!
